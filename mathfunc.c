
/**
 * @brief A basic adder function of 2 numbers
 * 
 * @param a 
 * @param b 
 * @return int 
 */
int adder(int a, int b)
{
	return a + b;
}

/**
 * @brief A basic multiplier function of 2 numbers
 * 
 * @param a 
 * @param b 
 * @return int 
 */
int multiply(int a, int b)
{
	return a * b;
}


/**
 * @brief A basic divider function of 2 numbers
 * 
 * @param a 
 * @param b 
 * @return int 
 */
int divide(int a, int b)
{
	return a / b;
}
